from core import *

print(f'并联系统{parallel(3, 0.9)}')#一个由三个零部件组成的并联系统，故障率均为0.9
print(f'并联系统{parallel(3, [0.9, 0.8, 0.7])}')#一个由三个零部件组成的并联系统，故障率分别为0.9,0.8,0.7
print(f'串联系统{series(3, 0.1)}')#一个由三个零部件组成的串联系统，故障率均为0.1
print(f'串联系统{series(3, [0.1, 0.2, 0.3])}')#一个由三个零部件组成的串联系统，故障率分别为0.1,0.2,0,3
print(f'表决系统{vote(5, 0.1 , 0.6)}')#一个由五个零部件组成的表决系统，故障率均为0.1，当未故障比例大于0.6时有效
print(f'表决系统{vote(5, 0.1, 3)}')#一个由五个零部件组成的表决系统，故障率均0.1，当未故障数量大于3时有效
